// Valid Anagram
// https://leetcode.com/problems/valid-anagram/submissions/

/**
 * @param {string} s
 * @param {string} t
 * @return {boolean}
 */
var isAnagram = function(s, t) {
    return s.split('').sort().join('') == t.split('').sort().join('')
};

